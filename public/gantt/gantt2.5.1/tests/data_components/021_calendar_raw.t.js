StartTest(function(t) {
    
    //======================================================================================================================================================================================================================================================
    t.diag('Sanity')

    t.ok(Gnt.data.Calendar, "Gnt.data.Calendar is here")
    t.ok(Gnt.model.Task, "Gnt.model.Task is here")

    var DATE        = Sch.util.Date
    
    //======================================================================================================================================================================================================================================================
    t.diag('Setup')
    
    
    var calendar        = new Gnt.data.Calendar({
        weekendsAreWorkdays    : true
    })

    var task            = new Gnt.model.Task()
    
    task.calendar       = calendar

    
    //======================================================================================================================================================================================================================================================
    t.diag('Duration')
    
    var duration = task.calculateDuration(Ext.Date.parse("2011-06-02T00:00:00", 'c'), Ext.Date.parse("2011-06-04T00:00:00", 'c'), DATE.DAY)
    
    t.ok(duration == 2, 'Correct duration (weekends are not taken into account)')
    
    
    
})    
